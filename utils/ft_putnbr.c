#include "ft_printf.h"

void	ft_putnbr(uintmax_t n)
{
	uintmax_t a;

	a = n;
	if (a < 0)
	{
		a = -a;
		ft_putchar('-');
	}
	if (a > 9)
		ft_putnbr(a / 10);
	ft_putchar(a % 10 + 48);
}
